#El último ejercicio consiste en extraer una porción de la lista en otra lista nueva. 
# La extracción se realiza utilizando la siguiente instrucción:
#Lista[n:m] La instrucción extraerá una nueva lista que empezará en el índice n y terminará en el m-1

lista = [1,2,3,4,5,6,7,8,9]
print(lista)
lista1 = lista[3:7]
print(lista1)
lista2 = lista[:5]
print(lista2)
lista3 = lista[6:]
print(lista3)